package Kisok;

public class Menu {
	private String num;
	private String name;
	private String detail;
	private int price;
	
	public String getNum() {
		return this.num;
	}

	public String getName() {
		return this.name;
	}
	public String getDetail() {
		return this.detail;
	}
	private int getPrice() {
		return this.price;
	}
	
}
